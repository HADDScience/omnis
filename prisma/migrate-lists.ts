async function main() {
  // ─── 카테고리 upsert ─────────────────────────────────────
  const [certCategory, gwajaeCategory] = await Promise.all([
    prisma.omnisCategory.upsert({
      where: { name: "인증 현황" },
      update: {},
      create: { name: "인증 현황", icon: "🏅", sortOrder: 10 },
    }),
    prisma.omnisCategory.upsert({
      where: { name: "매출 현황" },
      update: {},
      create: { name: "매출 현황", icon: "📊", sortOrder: 20 },
    }),
  ])
  console.log(`카테고리 준비 완료: "${certCategory.name}", "${gwajaeCategory.name}"`)

  // ─── 가점항목 리스트 카드 upsert ────────────────────────
  const existingGajum = await prisma.omnisCard.findFirst({
    where: { title: "가점항목 리스트", categoryId: certCategory.id },
  })

  if (existingGajum) {
    await prisma.omnisCard.update({
      where: { id: existingGajum.id },
      data: {
        content: { text: GAJUM_MARKDOWN },
        tags: ["인증", "가점", "중기부", "메디바이오"],
        version: existingGajum.version + 1,
        updatedAt: new Date(),
      },
    })
    console.log(`가점항목 리스트 카드 업데이트 완료 (id: ${existingGajum.id})`)
  } else {
    const newCard = await prisma.omnisCard.create({
      data: {
        categoryId: certCategory.id,
        title: "가점항목 리스트",
        content: { text: GAJUM_MARKDOWN },
        tags: ["인증", "가점", "중기부", "메디바이오"],
      },
    })
    console.log(`가점항목 리스트 카드 생성 완료 (id: ${newCard.id})`)
  }

  // ─── 과제 리스트 카드 upsert ────────────────────────────
  const existingGwajae = await prisma.omnisCard.findFirst({
    where: { title: "과제 리스트", categoryId: gwajaeCategory.id },
  })

  if (existingGwajae) {
    await prisma.omnisCard.update({
      where: { id: existingGwajae.id },
      data: {
        content: { text: GWAJAE_MARKDOWN },
        tags: ["과제", "지원사업", "R&D", "바이오"],
        version: existingGwajae.version + 1,
        updatedAt: new Date(),
      },
    })
    console.log(`과제 리스트 카드 업데이트 완료 (id: ${existingGwajae.id})`)
  } else {
    const newCard = await prisma.omnisCard.create({
      data: {
        categoryId: gwajaeCategory.id,
        title: "과제 리스트",
        content: { text: GWAJAE_MARKDOWN },
        tags: ["과제", "지원사업", "R&D", "바이오"],
      },
    })
    console.log(`과제 리스트 카드 생성 완료 (id: ${newCard.id})`)
  }

  console.log("완료")
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(() => prisma.$disconnect())
